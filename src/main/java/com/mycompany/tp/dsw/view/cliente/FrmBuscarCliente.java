    private void txtIDKeyReleased(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_txtIDKeyReleased
        String idText = txtID.getText().trim();
        List<Cliente> litaCliente = new ArrayList<>();
        Integer id = null;

        if (!idText.isEmpty()) {
            try {
                id = Integer.parseInt(idText);
                Cliente cliente = clienteMemory.buscarClientePorId(id);

                if (cliente != null) {
                    litaCliente.add(cliente);
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "El ID debe ser un número entero válido.", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }
            txtNombre.setEnabled(false);
        } else {
            litaCliente = clienteMemory.obtenerTodosLosClientes();
            txtNombre.setEnabled(true);
        }

        if (clienteForm != null) {
            clienteForm.mostrarTabla(litaCliente);
        }
    }// GEN-LAST:event_txtIDKeyReleased

    private void txtNombreActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_txtNombreActionPerformed
        String nombre = txtNombre.getText().trim();
        List<Cliente> listaCliente = new ArrayList<>();
        if (nombre.isEmpty()) {
            listaCliente = clienteMemory.obtenerTodosLosClientes();
        } else {
            listaCliente = clienteMemory.buscarClientePorNombre(nombre);
        }

        if (clienteForm != null) {
            clienteForm.mostrarTabla(listaCliente);
        }
    }// GEN-LAST:event_txtNombreActionPerformed

