    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String idTXT = txfID.getText().trim();
        String nombreTXT = txfNombre.getText().trim();
        List<Cliente> listaCliente = new ArrayList<>();

        if (!(idTXT.isEmpty()) || !(nombreTXT.isEmpty())) {

            if (!(idTXT.isEmpty())) {
                try {
                    Integer id = Integer.parseInt(idTXT);
                    Cliente cliente;
                    try {
                        cliente = clienteMemory.buscarClientePorId(id);
                        if (cliente != null) {
                            listaCliente.add(cliente);
                        }
                    } catch (ClienteNoEncontradoException ex) {
                        // Logger.getLogger(FrmBuscarCliente.class.getName()).log(Level.SEVERE, null,
                        // ex);
                        JOptionPane.showMessageDialog(this, "El cliente no ha sido encontrado", "Error",
                                JOptionPane.ERROR_MESSAGE);
                    }

                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "El ID debe ser un numero entero valido", "Error",
                            JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            if (!(nombreTXT.isEmpty())) {
                try {
                    listaCliente = clienteMemory.buscarClientePorNombre(nombreTXT);
                } catch (ClienteNoEncontradoException ex) {
                    JOptionPane.showMessageDialog(this, "El cliente no ha sido encontrado", "Error",
                            JOptionPane.ERROR_MESSAGE);
                    // Logger.getLogger(FrmBuscarCliente.class.getName()).log(Level.SEVERE, null,
                    // ex);
                }

            }
        } else {
            listaCliente = clienteMemory.obtenerTodosLosClientes();
        }

        if (clienteForm != null) {
            clienteForm.mostrarTabla(listaCliente);
        }

    }// GEN-LAST:event_btnBuscarActionPerformed

