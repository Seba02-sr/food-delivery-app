    private void txtIDKeyReleased(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_txtIDKeyReleased
        String idText = txtID.getText().trim();
        Integer id = Integer.parseInt(idText);
        List<Plato> platos = new ArrayList<>();
        List<Bebida> bebidas = new ArrayList<>();
        if (idText.isEmpty()) {
            txtNombre.setEnabled(true);
            switch (tipoCategoria) {
                case "Plato":
                    platos = platoMemory.obtenerTodosLosPlatos();
                    break;
                case "Bebida":
                    bebidas = bebidaMemory.obtenerTodasLasBebidas();
                    break;
            }
        } else {
            txtNombre.setEnabled(false);
            switch (tipoCategoria) {
                case "Plato":
                    Plato plato = (Plato) platoMemory.filtrarPorId(id);
                    if (plato != null) {
                        platos.add(plato);
                    }
                    break;
                case "Bebida":
                    Bebida bebida = (Bebida) platoMemory.filtrarPorId(id);
                    if (bebida != null) {
                        bebidas.add(bebida);
                    }
                    break;
                default:
                    break;
            }
        }

        if (itemMenuForm != null) {
            switch (tipoCategoria) {
                case "Plato":
                    itemMenuForm.mostrarItemMenuEnPantalla(platos);
                    break;
                case "Bebida":
                    itemMenuForm.mostrarItemMenuEnPantalla(bebidas);
                    break;
                default:
                    break;
            }
        }

    }// GEN-LAST:event_txtIDKeyReleased

