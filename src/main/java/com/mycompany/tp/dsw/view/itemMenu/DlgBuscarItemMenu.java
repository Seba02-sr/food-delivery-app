    private void txtNombreKeyReleased(java.awt.event.KeyEvent evt) {// GEN-FIRST:event_txtNombreKeyReleased
        String nombre = txtNombre.getText().trim();
        List<Plato> platos = new ArrayList<>();
        List<Bebida> bebidas = new ArrayList<>();

        if (nombre.isEmpty()) {
            if (itemMenuForm != null) {
                itemMenuForm.mostrarItemMenuEnPantalla(platoMemory.obtenerTodosLosItemMenu());
            }
        } else {
            switch (tipoCategoria) {
                case "Plato":
                    platos = platoMemory.buscarPlatoPorNombre(nombre);
                    break;
                case "Bebida":
                    bebidas = bebidaMemory.buscarBebidaPorNombre(nombre);
                    break;
            }
        }

        if (itemMenuForm != null) {
            switch (tipoCategoria) {
                case "Plato":
                    itemMenuForm.mostrarItemMenuEnPantalla(platos);
                    break;
                case "Bebida":
                    itemMenuForm.mostrarItemMenuEnPantalla(bebidas);
                    break;
            }
        }
    }// GEN-LAST:event_txtNombreKeyReleased

