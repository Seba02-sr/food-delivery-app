    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnModificarActionPerformed
        String id = txtID.getText();
        String nombre = txtNombre.getText();
        String precio = txtPrecio.getText();
        String tamano = txtTamano.getText();
        String volumen = txtVolumen.getText();
        String graduacionAlcoholica = jSpinnerGraduacionAlcoholica.getValue().toString();
        // Si es bebida sin alcohol, no dejar modificarla
        if (graduacionAlcoholica.equals("0")) {
            jSpinnerGraduacionAlcoholica.setEnabled(false);
        }
        String descripcion = jTextAreaDescripcion.getText();

        BebidaDto bebidaDto = new BebidaDto(id, nombre, descripcion, precio, descripcion, null, graduacionAlcoholica,
                tamano, volumen);
        System.out.println("ID bebida en frmModificarBebida: " + bebidaDto.getIdText());
        bebidaMemory.modificarBebida(bebidaDto);
        // tempo
        List<Bebida> bebidas = bebidaMemory.obtenerTodasLasBebidas();
        System.out.println("aca deberia estar todas las bebidas: " + bebidas);

        // destempo
        JOptionPane.showMessageDialog(null, "Bebida modificada exitosamente");
        this.dispose();
    }// GEN-LAST:event_btnModificarActionPerformed

